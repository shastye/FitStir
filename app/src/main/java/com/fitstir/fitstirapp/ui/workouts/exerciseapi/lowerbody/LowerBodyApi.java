package com.fitstir.fitstirapp.ui.workouts.exerciseapi.lowerbody;

public class LowerBodyApi {

    String lower_legs;
    String upper_legs;
    String waist;


    public String getLower_legs() {
        return lower_legs;
    }

    public void setLower_legs(String lower_legs) {
        this.lower_legs = lower_legs;
    }

    public String getUpper_legs() {
        return upper_legs;
    }

    public void setUpper_legs(String upper_legs) {
        this.upper_legs = upper_legs;
    }

    public String getWaist() {
        return waist;
    }

    public void setWaist(String waist) {
        this.waist = waist;
    }
}
